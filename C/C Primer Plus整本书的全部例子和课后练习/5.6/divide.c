//整数除法和浮点数除法
#include<stdio.h>

int main(void)
{
	printf("integer division 3/4 is %d \n",3/4);
	printf("integer division 7/6 is %d \n",7/6);
	printf("integer division 6/3 is %d \n",6/3);
	printf("float division 7./4. is %1.2f \n",7./4.);
	printf("mixed division 7./4 is %1.2f \n",7./4);
	return 0;
}